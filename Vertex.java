public class Vertex {
    int rec;
    String email;
    /**
     * Constructs a vertex with the given recommendation value and email address.
     *
     * @param rec   The recommendation value associated with the vertex.
     * @param email The email address associated with the vertex.
     */
    Vertex(int rec, String email) {
        this.rec = rec;
        this.email = email;
    }

}